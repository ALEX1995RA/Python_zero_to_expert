# Ejemplos de tipos de datos en Python

# tipo entero
edad = 30
print('edad: ',edad)

#Numero con Puento Flotante
altura = 1.75
print('Altura: ',altura)

#Cadena de Texto
nombre = 'Diana'
print('Nombre:', nombre)

#Tipo Boolean
es_estudiante = False
print('Es estudiante? ', es_estudiante)

#None, ausencia de Valor
dirección = None
print('Direccion:', dirección)

