# Metodos basicos de Cadenas

cadena1 ='Hola Mundo'
print(f'Cadena Original:{cadena1}')

# Aplicacion de Upper - Cambios a Mayusculas
mayusculas = cadena1.upper()
print(f'cadena en Mayusculas: {mayusculas}')

# Aplicacion de Lower - Cambio a Minusculas
print(f'Cadena en Minusculas: {cadena1.lower()}')

# Aplicacion de Strip -  Retiro de espacios inicio y final
cadena2 =' Juan Perez '
print(f'cadena sin espacios: {cadena2.strip()}')

 