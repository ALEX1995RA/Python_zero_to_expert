# Ciclo While Paython
print('*** Ciclo While ***')

# Imprimir los valores de 1 a 5
contador = 1
while contador <= 5:
    print(contador, end=' - ')
    print(contador, end=' ')
    print(contador)
    contador += 1

# Ciclo For Phyton
print('\n Itineracion de Caracteres')
print('*** Ciclo For ***')
cadena = 'Hola Mundo'
#Iteracion de caaracteres
for letra in cadena:
    print(letra, end=' ')

print('\n Recorremos la lista de frutas')
frutas = ['Platano', 'Fresa', 'Mango']
for fruta in frutas:
    print(fruta, end=' ')