# Ejemplos tipos Datos
 
# Ejemplo de copncatenacio o suma de valores
numero1_cadena = '10'
print(f'Número 1 en cadena{numero1_cadena}')
numero2_cadena = '20'
print(f'Número 2 en cadena: {numero2_cadena}')
resultado = numero1_cadena + numero2_cadena
print(f'Concatenación: {resultado}')

# Comvertir a Número entero
numero1_cadena = int(numero1_cadena)
numero2_cadena = int(numero2_cadena)
resultado = numero2_cadena + numero1_cadena
print(f'Suma de Valores enteros: {resultado}')

