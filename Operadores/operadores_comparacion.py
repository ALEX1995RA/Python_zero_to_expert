#Operadores de Comparacion o Relacionales
a, b = 7, 5
print(f' Valor Inicial de a: {a} y de b:{b}')

#operador Igual Igual = =
resultado = a == b
print(f'Resultado a == b es: {resultado}') 

#Operador Diferente !=
resultado = a != b
print(f'Resultado de a != b es: {resultado}')

#Operador Mayor Que 
resultado = a > b
print(f'Resultado de a > b es: {resultado}')

#Operador Mayor o Igual Que
resultado = a >= b
print(f'resultdao de a >= b: {resultado}')

#Operador Menor Que
resultado = a < b
print(f'Resultado de a < b es: {resultado}')

#Operador Menor o Igual Que
resultado = a <= b
print(f'Resultado a < b es: {resultado}')

