# Operador Logico Or simepre haya un valor Verdadero o True / regresa a True
condicion = False
condicion1= False
resultado = condicion or condicion1
print(f'Resultado {condicion} or {condicion1} es: {resultado}')

#Con un valor Verdadero o True
condicion = True
condicion1= False
resultado = condicion or condicion1
print(f'Resultado {condicion} or {condicion1} es: {resultado}')

#con ambos Valores Verdaderos o True
condicion = True
condicion1= True
resultado = condicion or condicion1
print(f'Resultado {condicion} or {condicion1} es: {resultado}')
